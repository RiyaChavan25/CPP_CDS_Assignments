#include <iostream>
using namespace std;

int main() {
    int age = 20;
    float pi = 3.14f;
    double distance = 12345.678;
    char grade = 'A';
    bool pass = true;

    cout << "Integer (age): " << age << "\n";
    cout << "Float (pi): " << pi << "\n";
    cout << "Double (distance): " << distance << "\n";
    cout << "Char (grade): " << grade << "\n";
    cout << "Boolean (pass): " << pass << "\n";

    return 0;
}
